## Ce que la garde de ferme implique concrètement

### Le rythme quotidien

Les animaux de ferme ont un **rythme fixe**. Les poules doivent être sorties le matin et rentrées le soir pour les protéger des prédateurs. Les chèvres et moutons ont besoin d''eau fraîche, de foin et d''une surveillance quotidienne.

Les chevaux demandent un entretien des box, un contrôle des clôtures et un suivi alimentaire précis.

### Les compétences nécessaires

- Savoir **reconnaître** un animal en détresse ou malade
- Être capable de manipuler du **matériel agricole** basique
- Connaître les **gestes d''urgence** : isoler un animal blessé, contacter le vétérinaire rural
- Avoir une **condition physique** suffisante pour les tâches physiques
- Supporter l''**isolement** et l''absence de commodités urbaines

### Les risques spécifiques

Clôtures endommagées, prédateurs (renards, rapaces pour les volailles), météo violente, pannes d''eau ou d''électricité.

> Un gardien de ferme doit savoir réagir seul, rapidement, avec bon sens. C''est ce qui fait la différence entre une garde réussie et une situation de crise.

