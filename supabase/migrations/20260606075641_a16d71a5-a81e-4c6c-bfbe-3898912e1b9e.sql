## <a id="prix"></a>Combien coûte le house sitting ?

Pour le **propriétaire**, le house sitting est **gratuit en lui-même**. Les seuls frais éventuels sont la cotisation à une plateforme de mise en relation (quelques euros par mois, souvent gratuite côté propriétaire). Économies typiques par rapport à une pension : **300 à 800 € pour deux semaines**.

Pour le **gardien** (*house sitter* ou *home sitter*), le house sitting permet de se loger gratuitement pendant la durée de la mission. C'est une économie réelle (50 à 150 € la nuit en location courte durée) en échange d'engagements précis. Aucune rémunération supplémentaire n'est due : c'est un échange équilibré, pas un emploi.

