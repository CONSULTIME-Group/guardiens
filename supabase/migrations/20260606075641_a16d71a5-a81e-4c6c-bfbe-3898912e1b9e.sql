## <a id="faq"></a>Questions fréquentes sur le house sitting

:::faq
**Le house sitting est-il vraiment gratuit ?**

Oui, des deux côtés. Le propriétaire ne paie pas le gardien, le gardien ne paie pas le logement. Seule une cotisation modeste à la plateforme de mise en relation peut s'appliquer.

**Quelle différence entre house sitting et home sitting ?**

Aucune. Ce sont deux noms pour la même pratique. *House sitting* est le terme anglo-saxon historique, *home sitting* la variante francisée. *Homesitting* (en un mot) est une orthographe alternative qu'on retrouve souvent en France.

**Quelle différence entre house sitting et pet sitting ?**

Le pet sitting est généralement **payant** et peut se faire en visites ponctuelles ou chez le pet sitter. Le house sitting implique de **vivre sur place sans rémunération**, en échange de l'hébergement gratuit.

**Est-ce qu'on est payé en house sitting ?**

Non. Le house sitter est logé gratuitement, c'est sa contrepartie. Aucun salaire n'est versé pour la garde des animaux ou la surveillance du logement.

**Comment fonctionne le house sitting concrètement ?**

Le propriétaire publie une annonce sur une plateforme spécialisée. Les gardiens candidatent. Une visio valide la confiance. Le jour J, passation des clés. Le gardien vit dans le logement, s'occupe des animaux, communique régulièrement. Au retour, état des lieux et avis croisés.

**Combien de temps dure une mission de house sitting ?**

De 2 jours à plusieurs mois. Les durées les plus fréquentes sont d'une à trois semaines, calquées sur les vacances scolaires françaises.

**Comment trouver des missions de house sitting ?**

Inscription sur une plateforme spécialisée, profil détaillé avec photo, candidatures à des missions adaptées à votre rythme. Commencez par des missions courtes pour construire vos premiers avis avant de viser des gardes longues.

**Peut-on faire du house sitting avec son propre animal ?**

Cela dépend de l'accord explicite du propriétaire et de la compatibilité avec les animaux du logement. À discuter en amont, jamais imposé.

**Le house sitting est-il légal en France ?**

Oui, sans ambiguïté. C'est un échange de services entre particuliers, sans contrepartie financière pour le logement. Ce n'est ni une location meublée, ni du travail dissimulé.

**Et si tout se passe mal pendant la mission ?**

Les plateformes sérieuses ont un support humain, un système d'avis, et parfois des gardiens d'urgence mobilisables en cas d'imprévu majeur.
:::

