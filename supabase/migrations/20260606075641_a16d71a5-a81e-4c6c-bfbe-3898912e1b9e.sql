## <a id="definition"></a>House sitting : définition précise

Le **house sitting** (littéralement « gardiennage de maison ») est un arrangement entre particuliers dans lequel :

- Un **propriétaire** s'absente (vacances, déplacement professionnel, hospitalisation, voyage long) et confie son logement à un tiers.
- Un **gardien** — appelé *house sitter*, *home sitter*, ou simplement gardien de maison — s'installe dans le logement pour la durée définie. Il prend en charge les missions convenues : soin des animaux, arrosage, courrier, entretien courant, présence dissuasive.
- Aucune somme n'est échangée pour le logement lui-même. Le gardien est hébergé gratuitement ; en contrepartie, il assure le service convenu.

Les termes **home sitting**, **homesitting** et **house sitting** désignent exactement la même pratique. *House minding* est la variante britannique. Tous renvoient au même principe : **logement gratuit contre présence et responsabilités**.

C'est ce qui distingue radicalement le house sitting du **pet sitting** payant, de la **location courte durée**, ou de la **pension animale**.

