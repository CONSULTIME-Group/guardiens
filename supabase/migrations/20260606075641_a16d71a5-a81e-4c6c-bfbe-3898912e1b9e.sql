## <a id="trouver-missions"></a>Comment trouver des missions de house sitting

Trois canaux principaux existent pour démarrer comme *house sitter* en France :

1. **Les plateformes spécialisées** (Guardiens et autres) : profils vérifiés, avis croisés, messagerie sécurisée, contrats types. C'est le canal le plus sûr pour des premières missions.
2. **Le bouche-à-oreille local** : famille, gens du coin, collègues. Fiable mais limité en volume.
3. **Les groupes communautaires** : associations locales, groupes de quartier. Variable, peu de garanties.

Pour construire un profil de *house sitter* attractif :

- **Photo claire**, présentation honnête, expérience animale réelle.
- **Premières missions courtes** (week-end, semaine) pour accumuler des avis avant de viser des gardes longues.
- **Disponibilité communiquée à l''avance** : les meilleures missions se réservent 6 à 10 semaines avant les dates.

