## <a id="remuneration"></a>Est-ce qu'on est payé en house sitting ?

**Non.** Le house sitting repose sur un échange non monétaire : **logement contre services**. Le house sitter n'est pas rémunéré pour son travail. Sa contrepartie est l'hébergement gratuit dans un logement souvent confortable et bien situé.

C'est ce qui distingue le house sitting du pet sitting (payé) et du salariat (statut, cotisations). Demander un salaire en plus du logement gratuit déséquilibre l'arrangement et sort du cadre du house sitting tel qu'il est compris en France et dans le monde.

Cas particuliers admis : **remboursement de frais réels** liés à la mission (kilomètres pour des sorties chez le vétérinaire, achats de nourriture animale convenus). Toujours discuté en amont, jamais imposé.

