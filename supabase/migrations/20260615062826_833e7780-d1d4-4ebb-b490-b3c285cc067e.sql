**Côté assurance**, vérifiez votre multirisque habitation (français si la maison est en France, local si elle est à l''étranger). La plupart des contrats couvrent la présence d''un tiers non rémunéré, mais une déclaration préalable peut être demandée pour une absence supérieure à 60 ou 90 jours. Un email à votre assureur suffit généralement à formaliser la situation.

**Côté visa**, votre gardien français entre dans votre pays d''accueil sous son propre statut touristique (90 jours dans la plupart des pays asiatiques, 90 jours Schengen pour un pays européen, ESTA pour les États-Unis, etc.). Il n''a aucun lien d''emploi avec vous, ce qui simplifie radicalement la situation. Prévenez-le simplement des règles locales : durée maximale, justificatif d''hébergement éventuel, billet retour exigé à la frontière.

