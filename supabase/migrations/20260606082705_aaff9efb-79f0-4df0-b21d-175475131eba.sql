## Qu'est-ce qu'un home sitter ?

Un home sitter est une personne de confiance qui s'installe au domicile d'un propriétaire pour s'occuper de sa maison et de ses animaux pendant son absence. Contrairement à une pension, l'animal reste dans son environnement, garde ses repères, et la maison est occupée — ce qui rassure tout le monde.

L'échange ne donne pas lieu à un paiement entre particuliers : le gîte gratuit est la contrepartie naturelle du service rendu.

## Qui peut devenir home sitter ?

Il n'y a pas de diplôme requis. Ce qui compte :

- Être majeur (18 ans minimum)
- Aimer sincèrement les animaux et savoir s'en occuper
- Être fiable, ponctuel, soigneux avec une maison qui n'est pas la vôtre
- Pouvoir se rendre disponible sur la durée d'une mission (quelques jours à plusieurs semaines)
- Accepter de vérifier votre identité pour rassurer les propriétaires

Une première expérience avec des animaux (perso, famille, bénévolat refuge, garde occasionnelle) facilite beaucoup le démarrage. Si vous n'en avez pas, commencez par de courtes missions ou de l'entraide ponctuelle pour construire votre réputation.

