Le home sitting consiste à garder une maison et ses animaux pendant l'absence des propriétaires, en logeant sur place. C'est une activité d'échange entre particuliers, encadrée, qui séduit de plus en plus de personnes en France : étudiants, retraités, télétravailleurs, amoureux des animaux.

