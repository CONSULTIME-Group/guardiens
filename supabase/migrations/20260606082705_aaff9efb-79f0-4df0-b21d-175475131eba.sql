## Étape 4 : assurez la mission comme un pro

Une mission réussie, c'est une mission où le propriétaire n'a aucun stress. Quelques règles simples :

- Arrivez à l'heure convenue, jamais en avance sans prévenir
- Lisez attentivement le guide de la maison (alimentation, médicaments, contacts vétérinaires)
- Envoyez des nouvelles régulières avec photos, sans submerger
- Respectez la maison comme si c'était la vôtre — ou mieux
- Signalez immédiatement tout incident, même mineur
- Au départ : tout en ordre, draps changés si convenu, poubelles sorties

## Combien de temps avant de vivre du home sitting ?

Soyons clairs : le home sitting n'est pas une activité rémunérée en France. Vous n'êtes pas payé en argent, vous économisez un logement. Pour un nomade, un télétravailleur ou quelqu'un en transition de vie, cela peut représenter plusieurs centaines d'euros par mois d'économies.

Comptez en général :

- 1 à 3 mois pour décrocher votre première mission
- 6 mois pour construire une réputation solide (statut Super Gardien)
- 1 an pour avoir un calendrier rempli en continu si vous êtes mobile

## Les erreurs qui plombent un démarrage

- Profil incomplet ou photo de mauvaise qualité
- Messages génériques copiés-collés
- Refuser systématiquement les missions courtes ou les petits animaux
- Promettre ce que vous ne pouvez pas tenir (présence permanente alors que vous travaillez)
- Négliger les avis : un avis non sollicité, c'est une réputation qui ne grandit pas

## Questions fréquentes

### Faut-il une assurance pour être home sitter ?

Votre responsabilité civile personnelle (incluse dans la plupart des assurances habitation) vous couvre dans la majorité des cas. Vérifiez les conditions auprès de votre assureur et signalez l'activité si elle devient régulière.

### Le home sitting est-il déclaré aux impôts ?

Non, tant que l'échange reste un service réciproque sans contrepartie financière entre particuliers. Si vous facturez vos services, c'est une autre activité (pet sitter professionnel) qui relève du statut de micro-entrepreneur.

### Combien de missions par an peut-on faire ?

Aucun plafond. Certains gardiens enchaînent les missions toute l'année, d'autres en font quelques-unes par saison. À vous de définir votre rythme.

### Peut-on être home sitter et propriétaire en même temps ?

Oui. Sur Guardiens, vous pouvez activer les deux espaces depuis votre compte. L'espace propriétaire est gratuit à vie.

