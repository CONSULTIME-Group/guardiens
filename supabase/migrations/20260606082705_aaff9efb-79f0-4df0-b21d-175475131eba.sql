**Postulez vite et bien.** Les missions partent en quelques heures. Activez les alertes par zone (jusqu'à trois zones géographiques) pour être prévenu dès qu'une annonce correspond à vos critères.

**Personnalisez chaque message.** Un copier-coller générique est éliminatoire. Citez le prénom de l'animal, mentionnez un détail de l'annonce, expliquez en deux phrases pourquoi vous êtes la bonne personne pour cette mission précise.

**Acceptez les missions courtes au début.** Un week-end ou trois jours suffisent pour décrocher un premier avis. Avec deux ou trois avis positifs, votre taux de réponse double.

