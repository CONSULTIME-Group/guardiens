## House sitting et législation française

Le house sitting entre particuliers, sans contrepartie financière pour le logement, **n''est pas soumis à un cadre commercial**. Ce n''est ni une location meublée, ni une activité salariée, ni du travail dissimulé.

Les bonnes pratiques recommandées :

- Formaliser l'arrangement par écrit (la plupart des plateformes proposent un contrat type).
- Vérifier l'assurance habitation (la plupart des contrats couvrent l'occupation temporaire par un tiers de confiance).
- Pour les gardes très longues, consulter sa compagnie d'assurance.

