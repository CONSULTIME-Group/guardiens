## Comment fonctionne le house sitting en pratique ?

### Étape 1 — La mise en relation

Sur une plateforme spécialisée (comme Guardiens), le propriétaire publie une annonce détaillée : dates, animaux concernés, missions attendues, particularités du logement. Les gardiens vérifiés candidatent ; le propriétaire choisit après échange.

### Étape 2 — La rencontre préalable

Une visioconférence ou une rencontre physique permet de valider la confiance mutuelle. C'est l'étape la plus importante : on ne confie pas ses animaux à un inconnu sans s'être parlé.

### Étape 3 — La passation

Le jour du départ, le propriétaire fait visiter, montre les habitudes de l'animal, remet les clés, partage les contacts utiles (vétérinaire, gens du coin de confiance). Un guide de maison écrit est recommandé.

### Étape 4 — Le gardiennage

Le gardien vit dans le logement, suit les routines de l'animal, communique régulièrement avec le propriétaire (photos, messages courts), et gère les imprévus dans le cadre convenu.

### Étape 5 — Le retour & l'évaluation

Au retour, état des lieux rapide, restitution. Sur les plateformes sérieuses, les deux parties laissent un avis qui alimente la réputation publique.

