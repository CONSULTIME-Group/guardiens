## House sitting : définition précise

Le house sitting est un **arrangement entre particuliers** dans lequel :

- Un **propriétaire** s'absente (vacances, déplacement professionnel, hospitalisation, voyage long) et confie son logement à un tiers.
- Un **gardien** (ou *house sitter*) s'installe dans le logement pour la durée définie, et prend en charge les missions convenues : soin des animaux, arrosage, courrier, entretien courant, présence dissuasive.
- Aucune somme n'est échangée pour le logement lui-même. Le gardien est hébergé gratuitement ; en contrepartie, il assure le service convenu.

C'est ce qui distingue radicalement le house sitting du pet sitting payant, de la location courte durée, ou de la pension animale.

