## FAQ house sitting

**Le house sitting est-il vraiment gratuit ?**
Oui pour le logement et la garde, des deux côtés. Seule une cotisation plateforme peut s'appliquer (modeste).

**Combien de temps dure une mission de house sitting ?**
De 2 jours à plusieurs mois. Les durées les plus fréquentes : 1 à 3 semaines.

**Est-ce que je peux faire du house sitting avec mon propre animal ?**
Cela dépend de l'accord du propriétaire et de la compatibilité avec les animaux du logement. À discuter en amont, jamais imposé.

**Quelle différence avec le pet sitting ?**
Le pet sitting est généralement payant et peut se faire en visites ponctuelles ou chez le sitter. Le house sitting implique de **vivre sur place** sans rémunération, en échange du logement.

**Et si tout se passe mal ?**
Les plateformes sérieuses ont un support humain, un système d'avis, et parfois des gardiens d'urgence mobilisables en cas d'imprévu.

