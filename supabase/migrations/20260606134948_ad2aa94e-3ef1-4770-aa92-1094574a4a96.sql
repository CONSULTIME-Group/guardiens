$ARTICLE$# Devenir Gardien Pro sur Guardiens : le guide complet

Guardiens accueille deux familles de gardiens : les particuliers passionnés qui rendent service, et les professionnels animaliers déclarés qui exercent ce métier. Pour distinguer clairement les seconds, et permettre aux propriétaires de choisir en pleine connaissance de cause, nous délivrons un badge **Gardien Pro vérifié**.

Cet article explique qui peut obtenir ce statut, quelles pièces fournir, comment se déroule la vérification, et ce que cela change pour vous et pour les propriétaires.

---

## Qui peut devenir Gardien Pro ?

Toute personne qui exerce une activité animalière déclarée en France peut demander le statut Pro. Les profils typiques :

- **Détenteur de l'ACACED** (Attestation de Connaissances pour les Animaux de Compagnie d'Espèces Domestiques), obligatoire pour exercer à titre professionnel auprès de chiens, chats ou NAC.
- **Éducateur canin, comportementaliste, dresseur** avec diplôme reconnu (Brevet Professionnel Éducateur Canin, Certificat de Capacité, formation Animalin, etc.).
- **Pension, refuge, micro-pension à domicile** disposant d'un numéro SIRET et déclaré en préfecture.
- **Dog-sitter, pet-sitter, cat-sitter** ayant créé une micro-entreprise dédiée à la garde d'animaux.
- **Auxiliaire vétérinaire (ASV), toiletteur, ostéopathe animalier** souhaitant proposer en parallèle des prestations de garde.

Le statut Pro n'est pas réservé aux grandes structures. Une auto-entreprise déclarée et un diplôme cohérent suffisent.

---

## Pourquoi un statut Pro distinct ?

La majorité des gardes sur Guardiens reposent sur la confiance entre particuliers, sans transaction financière directe : on garde l'animal d'un proche, on rend service, on échange une garde contre une autre. C'est notre cœur de promesse.

Mais une partie des propriétaires cherche une prise en charge **professionnelle, rémunérée, contractualisée**. Pour eux, il est essentiel de savoir qui exerce ce métier et qui le fait à titre amical. Le badge Pro répond à cette demande :

- **Transparence** : pas de surprise, pas d'ambiguïté sur la nature de la prestation.
- **Tarification libre** : un Pro peut afficher un tarif indicatif, le particulier ne le fait pas.
- **Responsabilité** : un Pro engage sa responsabilité professionnelle (assurance RC pro recommandée).
- **Visibilité** : pastille « Pro » sur les cartes de recherche et filtre dédié pour les propriétaires qui recherchent ce profil.

Guardiens reste hors du circuit financier : nous ne prélevons aucune commission sur les prestations Pro, vous facturez directement votre client.

---

## Pièces acceptées pour la vérification

Vous devez fournir **au moins une pièce probante** parmi :

**Diplômes et certifications**

- Attestation ACACED (chien, chat, NAC, équidé, selon votre périmètre)
- Certificat de Capacité d'entretien d'animaux d'espèces domestiques
- Brevet Professionnel Éducateur Canin
- Diplôme d'auxiliaire vétérinaire (ASV, AVQ, AVS)
- Formation comportementaliste reconnue (Animalin, FFAB, etc.)
- Toute formation animalière diplômante équivalente

**Pièces d'activité déclarée**

- Extrait Kbis ou avis SIRENE de moins de 3 mois
- Attestation d'inscription URSSAF (auto-entrepreneur)
- Attestation d'assurance Responsabilité Civile Professionnelle (recommandée mais non bloquante)

Le SIRET déclaré doit être **cohérent avec votre identité** et idéalement avec une activité animalière (codes NAF 96.09Z, 01.62Z, 75.00Z principalement, mais d'autres sont acceptés au cas par cas).

---

## Comment se déroule la vérification

1. **Vous déposez le document** depuis votre espace Paramètres > Sécurité, section Gardien Pro. Le fichier (PDF ou image) est stocké de manière privée et chiffrée.
2. **Notre IA analyse automatiquement** le document : type de pièce, nom du titulaire, SIRET le cas échéant, cohérence avec votre profil. L'analyse est quasi instantanée.
3. **Auto-validation si la confiance est élevée** (≥ 85%) et que les informations correspondent à votre profil. Vous recevez le badge dans la minute.
4. **Revue manuelle sinon** : un membre de l'équipe Guardiens examine votre dossier sous 48h ouvrées. En cas de refus, vous recevez le motif et pouvez redéposer un document.

Vous pouvez à tout moment retirer une pièce et redéposer une version plus lisible ou plus récente.

---

## Ce que change le badge Pro sur votre profil

Une fois validé, votre profil affiche :

- Une pastille **Pro vérifié** visible sur les cartes de recherche et sur votre profil public.
- Une **spécialité déclarée** (éducateur, pension, dog-sitter, ASV, etc.) que vous choisissez.
- Une **accroche libre** pour décrire votre approche en une phrase.
- Un **champ tarif libre** où vous indiquez ce qui vous convient : tarif horaire, forfait journée, fourchette, devis sur demande. À vous de cadrer.

Les propriétaires peuvent filtrer les résultats de recherche pour ne voir que les Gardiens Pro. Vous restez aussi visible dans les résultats classiques.

---

## Cadre déontologique attendu

En affichant le badge Pro, vous vous engagez à :

- Exercer dans le respect de la réglementation française (déclaration, ACACED le cas échéant, assurance).
- Être transparent sur vos tarifs et conditions avant toute prestation.
- Refuser une mission qui dépasse vos compétences (NAC sans expérience, animal réactif sans formation comportement, etc.).
- Maintenir vos pièces à jour. Une attestation périmée peut entraîner le retrait temporaire du badge.
- Respecter la charte Guardiens : bienveillance, sécurité de l'animal, communication claire avec le propriétaire.

Tout manquement signalé fait l'objet d'une revue. En cas de fraude documentaire avérée, le badge est retiré définitivement et le compte suspendu.

---

## Vie privée et conservation des documents

Vos pièces sont stockées dans un espace privé accessible uniquement à vous et à l'équipe de modération Guardiens. Elles ne sont jamais affichées publiquement. En cas de suppression de votre statut Pro ou de votre compte, les documents sont effacés selon notre politique de rétention (voir nos CGU et notre politique RGPD).

---

