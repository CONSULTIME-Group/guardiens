
UPDATE articles SET content = $CONTENT$# Premiers pas sur Guardiens : démarrer en 5 minutes

Ce guide est court. Guardiens est simple à prendre en main, voici l'essentiel pour démarrer efficacement, que vous soyez propriétaire ou gardien.

---

## Étape 1, Créer votre compte

Rendez-vous sur guardiens.fr et choisissez votre rôle : propriétaire ou gardien. L'inscription prend moins de 2 minutes : email, mot de passe, prénom, code postal. Le code postal sert uniquement à calculer la proximité avec les autres membres, il n'est jamais affiché publiquement.

Vous pouvez aussi activer les deux rôles depuis le même compte : un seul email, deux espaces. Beaucoup de membres font confiance à cette double activation, car la garde est par nature réciproque.

---

## Étape 2, Compléter votre profil

**Si vous êtes propriétaire :** renseignez votre logement (type, surface, jardin, photos), vos animaux (espèce, âge, caractère, habitudes alimentaires), et votre guide de la maison (consignes, contacts d'urgence, vétérinaire). Plus votre profil est précis, plus les candidatures reçues seront pertinentes.

**Si vous êtes gardien :** ajoutez votre photo (visage visible, sans filtre), une bio honnête, votre expérience animale (chiens, chats, NAC, durée), et vos disponibilités sur le calendrier. Visez 60 % minimum de complétion : c'est le seuil de visibilité dans les résultats de recherche. Au-delà de 80 %, vous remontez sur les annonces du coin.

Pensez à indiquer vos compétences spécifiques : administration de médicaments, jeune chien, animal âgé, garde longue durée. Ces filtres sont activement utilisés par les propriétaires.

---

## Étape 3, Faire vérifier votre identité

Téléchargez votre pièce d'identité (carte nationale ou passeport) et prenez un selfie en suivant les instructions à l'écran. Notre équipe valide sous 24 heures ouvrées. La vérification d'identité débloque le badge « Identité vérifiée » sur votre profil public, augmente fortement votre score de confiance et reste obligatoire pour échanger des coordonnées privées.

Vos documents sont chiffrés, conservés le temps strictement nécessaire à la vérification, et jamais partagés avec d'autres membres.

---

## Étape 4, Votre première action

**Propriétaire :** publiez votre première annonce. Décrivez clairement les dates, les animaux, le logement et ce que vous attendez du gardien. Une annonce complète reçoit en moyenne 3 fois plus de candidatures qu'une annonce minimale.

**Gardien :** activez le mode « Je suis disponible » et postulez aux annonces qui correspondent à votre zone et vos compétences. Personnalisez chaque message de candidature : un message générique a peu de chances d'aboutir.

Vous pouvez aussi répondre à des coups de main (petites missions sans nuitée) : promener un chien, donner des croquettes, surveiller un chat pendant un week-end. C'est un excellent moyen de construire votre réputation rapidement.

---

## Étape 5, Construire votre réputation

Chaque garde ou coup de main réussi donne lieu à un avis croisé. Vos premiers avis sont déterminants : soignez la communication, soyez à l'heure, envoyez des nouvelles régulières avec photos. Les profils notés au-dessus de 4,8/5 sur 5 avis obtiennent le statut Super Gardien.

---

## Jusqu'au 14 juillet 2026, tout est sans frais

Inscription, profil, annonces, candidatures, messagerie : tout est sans frais jusqu'au 14 juillet 2026. Les membres inscrits avant cette date obtiennent le statut Fondateur permanent, matérialisé par un sceau doré sur leur profil public.

Après le 14 juillet 2026, l'espace propriétaire reste sans frais pour toujours. Seul l'espace gardien passe à un abonnement standard (6,99 €/mois sans engagement, ou 12 € en achat unique). Les Fondateurs conservent leurs avantages historiques.

