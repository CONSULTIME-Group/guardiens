## Comment bien préparer la garde de votre chien

Quelle que soit la solution choisie, anticipez ces étapes :

1. **Réservez tôt** : 2 à 3 mois à l'avance pour juillet-août, 3-4 semaines pour les autres périodes.
2. **Rencontrez le gardien** avant le départ — idéalement deux fois pour que votre chien le reconnaisse.
3. **Préparez un dossier** : carnet de santé, ordonnances, contact vétérinaire (consultez notre liste [vétérinaires d'urgence à Lyon](/actualites/veterinaire-urgence-lyon-guide)), habitudes alimentaires, mots-clés d'éducation, jouets préférés. La <a href="https://www.service-public.fr/particuliers/vosdroits/F1996" rel="nofollow noopener" target="_blank">réglementation française sur la détention d'un chien</a> impose d'ailleurs un carnet de santé à jour.
4. **Laissez ses affaires habituelles** : panier, gamelle, jouets, couverture qui sent la maison.
5. **Donnez les clés et les codes** au gardien si la garde se fait chez vous, avec un voisin référent en cas d'urgence. Notre [checklist complète avant départ](/actualites/checklist-partir-vacances-animaux) reprend tous ces points.

