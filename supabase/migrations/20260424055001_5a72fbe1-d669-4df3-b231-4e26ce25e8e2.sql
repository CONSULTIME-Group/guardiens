Vous vous demandez peut-être comment Guardiens peut proposer un service sans frais aux propriétaires. La réponse est simple : nous croyons que **faire garder son animal ne devrait pas être un luxe**. La plateforme se finance uniquement via un abonnement gardien (6,99 €/mois), qui permet aux gardiens de proposer leurs services en toute transparence et de bénéficier d'outils pro (vérification d'identité, badges, messagerie sécurisée). Il n'y a **aucune commission sur les gardes** — ce que vous donnez au gardien, ou le service que vous lui rendez en retour, reste entièrement entre vous. Pour les détails, voir notre page [tarifs](/tarifs).

Cette logique s'inscrit dans une démarche plus large d'[entraide locale et de réseau de quartier](/actualites/reseau-entraide-quartier-lyon-aura), qui dépasse la simple garde d'animal.

## Et si vous deveniez aussi gardien à Lyon ?

Beaucoup de propriétaires lyonnais utilisent Guardiens dans les deux sens : ils font garder leur chien quand ils partent, et accueillent un chien voisin quand ils restent. Pour vous lancer, lisez nos [conseils pour rédiger un profil de gardien qui inspire confiance](/actualites/conseil-gardien-creer-profil-attractif-lyon).

:::faq
**Combien coûte vraiment Guardiens pour un propriétaire à Lyon ?**
0 € pour le propriétaire. Aucune commission, aucun frais d'inscription, aucun abonnement. Vous publiez votre annonce, vous échangez avec les gardiens du quartier, vous choisissez. C'est tout.

**Combien de temps à l'avance dois-je publier mon annonce ?**
Idéalement 3 à 4 semaines avant pour les vacances d'été, 1 à 2 semaines pour un week-end. Plus c'est tôt, plus vous avez de gardiens disponibles à rencontrer en amont.

**Comment savoir si je peux faire confiance à un gardien Guardiens ?**
Chaque gardien dispose d'un profil détaillé (présentation, expérience, photos), d'avis publics laissés par d'autres propriétaires, de badges (gardien vérifié, super gardien) et peut justifier son identité. Vous pouvez échanger librement avant de décider, et organiser une rencontre préalable.

**Que se passe-t-il en cas d'urgence pendant ma garde ?**
Vous gardez le contact direct avec votre gardien via la messagerie. En cas d'urgence vétérinaire, le gardien peut vous joindre 24h/24. Nous recommandons de toujours laisser le contact de votre vétérinaire (voir notre [liste des urgences vétérinaires à Lyon](/actualites/veterinaire-urgence-lyon-guide)) et un voisin référent.

**Et si je n'ai pas de gardien dans mon quartier ?**
Lyon est très bien couverte. Si vraiment aucun gardien n'est disponible immédiatement, créez une alerte et nous vous notifions dès qu'un nouveau gardien rejoint votre zone. Vous pouvez aussi élargir votre recherche aux communes voisines.

**Puis-je faire garder mon chat aussi ?**
Bien sûr. Guardiens couvre toutes les espèces domestiques. Pour les chats, lisez notre dossier [garde de chat à Lyon à domicile](/actualites/garde-chat-domicile-lyon).
:::

